class Hotel {
    public static int[] roomNumbers(int[] rooms) {
        return rooms;
    }

    public static double[] roomSizes(double[] sizes) {
        return sizes;
    }

    public static float[] roomPrices(float[] prices) {
        return prices;
    }

    public static char[] ratings(char[] stars) {
        return stars;
    }

    public static boolean[] availability(boolean[] available) {
        return available;
    }

    public static long[] bookings(long[] bookingIds) {
        return bookingIds;
    }

    public static String[] hotelNames(String[] names) {
        return names;
    }
}
