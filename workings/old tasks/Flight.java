class Flight {
    public static int[] flightNumbers(int[] numbers) {
        return numbers;
    }

    public static double[] flightSpeeds(double[] speeds) {
        return speeds;
    }

    public static float[] ticketPrices(float[] prices) {
        return prices;
    }

    public static char[] ratings(char[] grades) {
        return grades;
    }

    public static boolean[] availability(boolean[] available) {
        return available;
    }

    public static long[] distances(long[] kms) {
        return kms;
    }

    public static String[] airlines(String[] names) {
        return names;
    }
}